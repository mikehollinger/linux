static void cflash_slave_destroy(struct scsi_device *sdev)
{
	struct lun_info *p_lun_info = sdev->hostdata;

	if (p_lun_info) {
		sdev->hostdata = NULL;
		list_del(&p_lun_info->list);
		ba_terminate(&p_lun_info->blka.ba_lun);
		kfree(p_lun_info);
	}

	cflash_info("p_lun_info=%p", p_lun_info);
	return;
}

